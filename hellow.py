a = 235
b = 5
print(a + b)    # 240
print(a - b)    # 230
print(a * b)    # 1175
print(a / b)    # 47.0

'''
使用type()检查变量地类型

Version：0.01
Author：浅色文
'''

a = 200
b = 20.35
c = 1 + 5j
d = 'hello,world'
e = True

print(type(a)) #整数'int'
print(type(b)) #浮点数'float'
print(type(c)) #复数型'complex'
print(type(d)) #字符串'str'
print(type(e)) #布尔型 'bool'

